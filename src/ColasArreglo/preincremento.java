public class preincremento {
    
    public static void main(String[] args) {
        
        int i = 0;
        int k = 0;
        
        for (int j = 0; j < 5; j++) {
            
            System.out.println("i: "+ i++ + " k: "+ ++k);
            
        }
        
    }
    
}
